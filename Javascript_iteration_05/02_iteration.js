// while loop
let index = 0
while (index <= 10) {
    console.log(`Value of index is ${index}`);
    index = index + 2
}

let myarr = ["flash", "batman", "superman"]

let arr = 0
while (arr < myarr.length) {
    console.log(`value is : ${myarr[arr]}`);
    arr++
}

// do while loop
let score = 11
do {
    console.log(`${score}`);
    score++
} while (score < 10);